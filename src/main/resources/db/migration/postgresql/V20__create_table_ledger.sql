CREATE TABLE journal_entry
(
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id     BIGINT       NOT NULL,
    date        DATE         NOT NULL,
    description VARCHAR(255) NOT NULL,
    status      VARCHAR(50)  NOT NULL DEFAULT 'COMPLETED',
    created_at  TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at  TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_journal_entry_user FOREIGN KEY (user_id) REFERENCES "user" (id) ON DELETE CASCADE
);

CREATE INDEX idx_journal_entry_user_date ON journal_entry (user_id, date);

COMMENT ON COLUMN journal_entry.status IS 'Status esperadas: COMPLETED, PENDING, CANCELLED';

